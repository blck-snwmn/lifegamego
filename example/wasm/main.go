func main() {
	const (
		tickNum  = 200
		height   = 30
		width    = 38
		cellSize = 10
	)
	document := js.Global().Get("document")
	canvas := document.Call("getElementById", "canvas")
	canvas.Set("height", js.ValueOf(height*cellSize))
	canvas.Set("width", js.ValueOf(width*cellSize))
	time.Sleep(100 * time.Millisecond)

	canvasCtx := canvas.Call("getContext", "2d")
	canvasCtx.Call("scale", 1, 1)

	cs, d := lifegame.New(width, height, tickNum, glider)
	ctx, cancel := context.WithCancel(context.Background())
	defer cancel()
	cs.Start(ctx)

	fmt.Println("start lifegame")
	for i := 0; i < tickNum; i++ {
		for l, line := range d {
			for c, column := range line {
				l := l
				c := c
				s := <-column
				var color string
				if s.IsAlive() {
					color = "white"
				} else {
					color = "black"
				}
				canvasCtx.Set("fillStyle", color)
				canvasCtx.Call("fillRect", c*cellSize, l*cellSize, cellSize, cellSize)
			}
		}
		time.Sleep(100 * time.Millisecond)
	}
	fmt.Println("end lifegame")
	select {}
}
